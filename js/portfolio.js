const gallery = document.getElementById('portfolio-gallery');
const featuredImage = document.getElementById('featured-portfolio-image');
const featuredTitle = document.getElementById('featured-portfolio-title');
const featuredDescription = document.getElementById('featured-portfolio-description');
const categoryButtons = document.querySelectorAll('.category-pill');
const modal = document.getElementById('gallery-modal');
const closeModalButton = document.getElementById('gallery-close');
const mainImage = document.getElementById('gallery-main-image');
const modalTitle = document.getElementById('gallery-title');
const modalDescription = document.getElementById('gallery-description');
const modalThumbnails = document.getElementById('gallery-thumbnails');
const prevButton = document.getElementById('gallery-prev');
const nextButton = document.getElementById('gallery-next');

let activeCategory = 'wedding';
let currentIndex = 0;

function renderCategory(category) {
  activeCategory = category;
  const items = portfolioData[category] || [];
  gallery.innerHTML = '';

  items.forEach((item, index) => {
    const button = document.createElement('button');
    button.type = 'button';
    button.className = 'portfolio-card-button';
    button.innerHTML = `
      <article class="portfolio-card">
        <img src="${item.image}" alt="${item.title}" />
        <h3>${item.title}</h3>
        <p>${item.description}</p>
      </article>
    `;
    button.addEventListener('click', () => openGallery(category, index));
    gallery.appendChild(button);
  });

  const featured = featuredContent[category] || featuredContent.wedding;
  featuredImage.src = featured.image;
  featuredImage.alt = featured.title;
  featuredTitle.textContent = featured.title;
  featuredDescription.textContent = featured.description;
}

function updateGalleryModal() {
  const items = portfolioData[activeCategory] || [];
  const item = items[currentIndex] || items[0];
  if (!item) return;

  mainImage.src = item.image;
  mainImage.alt = item.title;
  modalTitle.textContent = item.title;
  modalDescription.textContent = item.description;

  modalThumbnails.innerHTML = '';
  items.forEach((thumb, index) => {
    const thumbButton = document.createElement('button');
    thumbButton.type = 'button';
    thumbButton.className = `gallery-thumb${index === currentIndex ? ' active' : ''}`;
    thumbButton.innerHTML = `<img src="${thumb.image}" alt="${thumb.title}" />`;
    thumbButton.addEventListener('click', () => {
      currentIndex = index;
      updateGalleryModal();
    });
    modalThumbnails.appendChild(thumbButton);
  });
}

function openGallery(category, index) {
  activeCategory = category;
  currentIndex = index;
  updateGalleryModal();
  modal.classList.add('open');
  modal.setAttribute('aria-hidden', 'false');
  document.body.style.overflow = 'hidden';
}

function closeGallery() {
  modal.classList.remove('open');
  modal.setAttribute('aria-hidden', 'true');
  document.body.style.overflow = '';
}

categoryButtons.forEach((button) => {
  button.addEventListener('click', () => {
    categoryButtons.forEach((item) => item.classList.remove('active'));
    button.classList.add('active');
    renderCategory(button.dataset.category);
  });
});

closeModalButton.addEventListener('click', closeGallery);
modal.addEventListener('click', (event) => {
  if (event.target === modal) closeGallery();
});
prevButton.addEventListener('click', () => {
  const items = portfolioData[activeCategory] || [];
  currentIndex = (currentIndex - 1 + items.length) % items.length;
  updateGalleryModal();
});
nextButton.addEventListener('click', () => {
  const items = portfolioData[activeCategory] || [];
  currentIndex = (currentIndex + 1) % items.length;
  updateGalleryModal();
});
document.addEventListener('keydown', (event) => {
  if (event.key === 'Escape' && modal.classList.contains('open')) {
    closeGallery();
  }
});

